// Math

console.log(`PI : ${Math.PI}`); // PI : 3.141592653589793
console.log(`내림 : ${Math.floor(8.5312)}`); // 8
console.log(`올림 : ${Math.ceil(2.123)}`); // 3
console.log(`반올림 : ${Math.round(3.512)}`); // 4
console.log(`절대값 : ${Math.abs(-3)}`); // 3
console.log(`최대값 : ${Math.max(5, 6, 8, 12, 1)}`); // 12
console.log(`0~1 난수 : ${Math.random()}`); // 0.19499620777325055

console.log(`0 ~ 9 : ${Math.floor(Math.random() * 10 + 1)}`); // 0 ~ 9 : 6

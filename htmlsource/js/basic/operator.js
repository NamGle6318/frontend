// 산술, 비교, 연결

let a = 4;
let b = 5;

console.log("a + b = ", a + b); // 9  
console.log("a - b = ", a - b); // -1
console.log("a * b = ", a * b); // 20
console.log("a / b = ", a / b); // 0.8 (자바는 몫만 나옴)
console.log("a % b = ", a % b); // 4

let c = "3";
console.log("a + c = ", a + c); // 43 = +가 연결의 역할


